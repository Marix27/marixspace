import streamlit as st

# título de la página
st.title("Transformaciones geometricas")

# Subtítulo
st.subheader("¿Que son?")

# texto
st.write(
    """
    Una transformación geométrica es una operación matemática que permite obtener una nueva figura a partir de una figura original, modificando sus propiedades, 
    como su posición, tamaño, ángulos o forma. 
    Al realizar una transformación, es posible alterar aspectos específicos de la figura, como: moverla, rotarla, reflejarla, ampliarla o reducirla.
    Dependiendo del tipo de transformación, la figura puede cambiar de diversas maneras, pero siempre mantiene ciertas propiedades invariables, como la forma o el área (en algunos casos).
    La nueva figura obtenida tras aplicar la transformación se denomina homóloga o transformada, y guarda una relación específica con la figura original, ya sea por congruencia, semejanza o simetría.

    """)


